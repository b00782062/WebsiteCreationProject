    <div class="podcasts">
        <p>In the first half of 2021, I was consuming a lot of articles online, I was reading and listening to a lot of books, podcasts, etc, and many topics resonated with the kind of life I was trying to build for myself. </p>
        <p>But I was just saving everything up as voice notes on a WhatsApp chat(meant for myself) or was writing and saving it up in Notion(yeah I know pretty scattered stuff)</p>
        <p>The idea of this podcast popped one day while I was showering. </p>
        <p>I used to always think of podcast shows as something only meant to motivate others. </p>
        <p>But then I decided, the podcast that I will start will serve 2 purposes. </p>
        <ol><li>It can motivate and inspire others</li><li>While it can also remind me about the things that I read and found helpful and want to revisit someday in the future. </li></ol>
        <p>It will literally be a click away and I don’t need to search for it within endless folders and apps. </p>
        <p>Does that make sense? </p>
